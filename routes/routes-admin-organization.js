'use strict';

const router = require('koa-router')(); // router middleware for koa
const organization  = require('../models/organization.js');

router.post('/organization/:organizationID/logo', organization.addLogo);
router.get('/organization/:organizationID', organization.get);
router.get('/organization', organization.getAll);
router.post('/organization', organization.add);
router.put('/organization/:organizationID', organization.update);

/* - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -  */

module.exports = router.middleware();
